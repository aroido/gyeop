import { readFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const TEMPLATE_ID = "11111111-1111-4111-8111-111111111111";
const VERSION_ID = "15151515-1515-4515-8515-151515151515";

function sqlString(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

export function renderPackSeed(pack) {
  const rows = pack.cards.map((card) =>
    [
      VERSION_ID,
      card.id,
      card.position,
      card.ownerPrompt,
      card.visitorPrompt,
      card.optionA,
      card.optionB,
      card.isSignature,
    ]
      .map((value, index) =>
        index === 2 || index === 7 ? String(value) : sqlString(value),
      )
      .join(", "),
  );

  return `-- Generated from content/packs/old-friend-v1.json by scripts/render-pack-seed.mjs.
-- Do not edit this file directly.
begin;

insert into public.pack_templates (
  id,
  slug,
  title,
  target_relationship,
  sensitivity,
  is_active
)
values (
  ${sqlString(TEMPLATE_ID)},
  ${sqlString(pack.slug)},
  ${sqlString(pack.title)},
  ${sqlString(pack.targetRelationship)},
  ${sqlString(pack.sensitivity)},
  ${pack.active}
);

insert into public.pack_versions (id, template_id, version)
values (
  ${sqlString(VERSION_ID)},
  ${sqlString(TEMPLATE_ID)},
  ${sqlString(pack.version)}
);

insert into public.pack_cards (
  pack_version_id,
  id,
  position,
  owner_prompt,
  visitor_prompt,
  option_a,
  option_b,
  is_signature
)
values
${rows.map((row, index) => `  (${row})${index === rows.length - 1 ? ";" : ","}`).join("\n")}

select public.publish_pack_version(${sqlString(VERSION_ID)});

commit;
`;
}

export function readPackManifest(root = ROOT) {
  return JSON.parse(
    readFileSync(path.join(root, "content/packs/old-friend-v1.json"), "utf8"),
  );
}

if (
  process.argv[1] &&
  path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)
) {
  process.stdout.write(renderPackSeed(readPackManifest()));
}
